// Pythagoras theorem
// http://www.codechef.com/COOK45/problems/SNAPE
#include <stdio.h>
#include <string.h>
#include <limits.h>
#include <math.h>
#include <stdlib.h>
#include <ctype.h>
#include <assert.h>

#define FOR(c, m) for(int c=0;c<(m);c++)
#define FORE(c, f, t) for(int c=(f);c<(t);c++)


char c = 0;
int getInt() {
    int r = 0;
    while (!(c >= '0' && c <= '9')) c = getc_unlocked(stdin);
    while (c >= '0' && c <= '9') {
        r = r * 10 + (c - '0');
        c = getc_unlocked(stdin);
    }
    return r;
}


int main(void) {
    int t = getInt();
    FOR(tt, t) {
        double b = getInt();
        double ls = getInt();
        double min = sqrt(ls * ls - b * b);
        double max = sqrt(ls * ls + b * b);
        printf("%lf %lf\n", min, max);
    }
}
